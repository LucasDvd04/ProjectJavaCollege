    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
            DiscipDAO ddao = new DiscipDAO();
            if(jTnome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o nome da Turma!");
            jTnome.requestFocus();
        }else{
           if(jTsala.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a 'Sala'!");
            jTsala.requestFocus();}
           else{
               if(jThoras.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(null, "Preencha a 'Carga Horaria'!");
                jThoras.requestFocus();}
               else{
            if(jCturma.getSelectedItem().equals("<Selecionar>")){
                JOptionPane.showMessageDialog(null, "Escolha o curso!");
                jCturma.requestFocus();
            }
            else{
                try{
                Disciplina disp = (Disciplina) jCturma.getSelectedItem();
                disp.setNomeD(jTnome.getText());
                disp.setSala(Integer.valueOf(jTsala.getText()));
                disp.setHora(Integer.valueOf(jThoras.getText()));
                if(ddao.verificarDiscp(jTnome.getText())){
                    JOptionPane.showMessageDialog(null,"A Disciplina "+disp.getNomeD()+" j√° existe");
                }
                else{
                ddao.validaDisciplina(disp);
                ddao.CadastrarDisciplina(disp);
                JOptionPane.showMessageDialog(null,"Disciplina inserida!");
            
                
                jTnome.setText(null);
                jCturma.setSelectedItem(null);
                jTnome.requestFocus();
                
                }
                }
                catch(Exception ex) {
                    System.out.println("erro" + ex);  
                }
            }
        }
           }
            }
    }//GEN-LAST:event_jButton1ActionPerformed

