    private void jBcadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcadastrarActionPerformed
        TurmaDAO tdao = new TurmaDAO();  
        if(jTnome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o nome da Turma!");
            jTnome.requestFocus();
        }else{
           
            if(jCcurso.getSelectedItem().equals("<Selecionar>")){
                JOptionPane.showMessageDialog(null, "Escolha o curso!");
                jCcurso.requestFocus();
            }else{
                try{
                Turma turma = (Turma) jCcurso.getSelectedItem();
                turma.setNomeTurma(jTnome.getText());
                if(tdao.verificarTurma(jTnome.getText())){
                    JOptionPane.showMessageDialog(null,"A turma "+turma.getNomeTurma()+" j√° existe");
                }
                else{
                tdao.validaTurma(turma);
                tdao.CadastrarTurma(turma);
                JOptionPane.showMessageDialog(null,"Turma inserida!");
            
                
                jTnome.setText(null);
                jCcurso.setSelectedItem(null);
                jTnome.requestFocus();
                
                }
                }
                catch(Exception ex) {
                    System.out.println("erro" + ex);  
                }
            }
        }          
    }//GEN-LAST:event_jBcadastrarActionPerformed

