    private void jBcadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcadastrarActionPerformed
        // TODO add your handling here:
         ProfDAO pdao = new ProfDAO();
        if(jTnome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o nome do professor!");
            jTnome.requestFocus();
        }else{
           if(jTemail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o email!");
            jTemail.requestFocus();}
           else{
            if(jCdiscp.getSelectedItem().equals("<Selecionar>")){
                JOptionPane.showMessageDialog(null, "Escolha a disciplina!");
                jCdiscp.requestFocus();
            }else{
                if(jTidade.getText().trim().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Preencha a idade!");
                    jTidade.requestFocus();}
                else{
                    if(jTuser.getText().trim().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Preencha o nome de usuario!");
                    jTuser.requestFocus();}
                    else{
                        if(jTpass.getText().trim().isEmpty()){
                           JOptionPane.showMessageDialog(null, "Preencha a senha!");
                           jTpass.requestFocus();
                    }
                
                try{
                Professor professor = (Professor) jCdiscp.getSelectedItem();
                professor.setNome(jTnome.getText());
                professor.setEmail(jTemail.getText());
                professor.setIdade(Integer.valueOf(jTidade.getText()));
                professor.setUsuario(jTuser.getText());
                professor.setSenha(jTpass.getText());
                if(pdao.verificarProf(jTemail.getText())){
                    JOptionPane.showMessageDialog(null,"O email "+professor.getEmail()+" ja esta cadastrado:");
                }
                else{
                     if(pdao.verificarU(jTuser.getText())){
                     JOptionPane.showMessageDialog(null,"O usuario "+professor.getUsuario()+" ja esta cadastrado:");
                }else{
                pdao.validaProf(professor);
                pdao.inserirProf(professor);
                pdao.inserirProfL(professor);
            
                
                jTnome.setText(null);
                jCdiscp.setSelectedItem(null);
                jTemail.setText(null);
                jTidade.setText(null);
                jTuser.setText(null);
                jTpass.setText(null);
                jTnome.requestFocus();
                
                
                }
                }
             }
                catch(Exception ex) {
                    System.out.println("erro" + ex);  
                }
            }
        }  
      }
    }
        }
    }//GEN-LAST:event_jBcadastrarActionPerformed

